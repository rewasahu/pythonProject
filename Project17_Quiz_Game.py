question_number = 0
score=0
def check_answer(guess,correct_answer,current_score):
    if guess == correct_answer:
        print("You got it right.")
        print(f"The correct answer is {correct_answer}")
        return current_score + 1
    else:
        print("That's wrong.")
        print(f"The correct answer is {correct_answer}")
        return current_score
    
    
def question():
    return question_data[question_number]["question"]

def answer():
    return question_data[question_number]["correct_answer"].lower()

game_continue = True
while game_continue:
    serial_number = question_number+1
    guess = input(f"Q.{serial_number}: {question()}. (True/False): ").lower()
    score = check_answer(guess,answer(),score)
    print(f"Your current score is: {score}/{serial_number}")
    question_number +=1
    if serial_number==len(question_data):
        print(f"\nYou've completed the quiz")
        print(f"Your final score was: {score}/{serial_number}")
        game_continue = False
    