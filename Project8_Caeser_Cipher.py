def coding(direction, message, shift):
    result = ""
    if direction == "decode":
        shift *=-1
    for letter in message:
        if letter not in alphabet:
            result+=letter
        else:
            position = alphabet.index(letter)
            new_position= position+shift
            result+=alphabet[new_position]
    return result


